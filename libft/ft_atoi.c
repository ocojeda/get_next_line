#include "libft.h"

int		ft_atoi(const char *str)
{
	int	negative;
	int	i;
	int	nb;

	nb = 0;
	i = 0;
	negative = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		negative = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
		nb = nb * 10 + str[i++] - '0';
	return (negative * nb);
}
