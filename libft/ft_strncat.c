#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	j;
	int		i;
	size_t	k;

	j = ft_strlen(s1);
	i = 0;
	k = 0;
	while (s2[i] != '\0' && k < n)
	{
		s1[j + i] = s2[i];
		i++;
		k++;
	}
	s1[j + i] = '\0';
	return (s1);
}
