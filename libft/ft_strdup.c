#include "stdlib.h"

char			*ft_strdup(const char *s1)
{
	int			i;
	int			l;
	char		*s2;

	i = 0;
	l = 0;
	while (s1[l] != '\0')
		l++;
	s2 = (char*)malloc(sizeof(*s2) * (l + 1));
	if (!s2)
		return (0);
	while (i < l)
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
