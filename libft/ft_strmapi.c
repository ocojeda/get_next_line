#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		n;
	char	*str;

	i = 0;
	if (!s || !f)
		return (NULL);
	n = ft_strlen((char*)s);
	if ((str = (char*)malloc(sizeof(char) * (n + 1))) == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
